  function pick(lang, key, table) {
    const order = lang === "en" ? ["en", "sw"] : ["sw", "en"];
    for (let i = 0; i < order.length; i++) {
      const pack = table[order[i]];
      if (pack && pack[key]) {
        return pack[key];
      }
    }
    return "";
  }

  function t(lang, key) {
    return pick(lang, key, MGRID_STRINGS);
  }

  function th(lang, key) {
    return pick(lang, key, MGRID_HTML);
  }

  function applyLanguage(lang) {
    if (!MGRID_STRINGS[lang]) {
      lang = "sw";
    }
    document.querySelectorAll("[data-i18n]").forEach(function (el) {
      const key = el.getAttribute("data-i18n");
      if (key) {
        const s = t(lang, key);
        if (s) {
          el.textContent = s;
        }
      }
    });
    document.querySelectorAll("[data-i18n-html]").forEach(function (el) {
      const key = el.getAttribute("data-i18n-html");
      if (key) {
        const s = th(lang, key);
        if (s) {
          el.innerHTML = s;
        }
      }
    });
    document.querySelectorAll("[data-i18n-placeholder]").forEach(function (el) {
      const key = el.getAttribute("data-i18n-placeholder");
      if (key) {
        const s = t(lang, key);
        if (s) {
          el.setAttribute("placeholder", s);
        }
      }
    });
    try {
      localStorage.setItem("mgrid_lang", lang);
    } catch (e) {
      /* ignore */
    }
    document.documentElement.setAttribute("lang", lang === "sw" ? "sw" : "en");
    document.querySelectorAll(".mgrid-lang-toggle label[for='mgridLangEn']").forEach(function (lbl) {
      lbl.setAttribute("title", t(lang, "lang.en"));
    });
    document.querySelectorAll(".mgrid-lang-toggle label[for='mgridLangSw']").forEach(function (lbl) {
      lbl.setAttribute("title", t(lang, "lang.sw"));
    });
  }

  function getMeta(name) {
    const m = document.querySelector('meta[name="' + name + '"]');
    return m ? m.getAttribute("content") || "" : "";
  }

  function persistLanguage(lang) {
    const url = getMeta("mgrid-set-lang-url");
    const csrf = getMeta("mgrid-csrf-token");
    if (!url || !csrf) {
      return Promise.resolve();
    }
    return fetch(url, {
      method: "POST",
      headers: { "Content-Type": "application/json", Accept: "application/json" },
      body: JSON.stringify({ lang: lang, _csrf: csrf }),
      credentials: "same-origin",
    }).then(function (res) {
      return res.json().catch(function () {
        return {};
      });
    });
  }

  function initLangToggle() {
    const fromDoc = document.documentElement.getAttribute("data-mgrid-default-lang");
    let initial = "sw";
    if (fromDoc === "sw" || fromDoc === "en") {
      initial = fromDoc;
    }
    try {
      const stored = localStorage.getItem("mgrid_lang");
      if (stored === "sw" || stored === "en") {
        initial = stored;
      }
    } catch (e2) {
      /* ignore */
    }
    const radios = document.querySelectorAll('input[name="mgridLang"]');
    if (radios.length) {
      radios.forEach(function (r) {
        r.checked = r.value === initial;
        r.addEventListener("change", function () {
          applyLanguage(r.value);
          persistLanguage(r.value).catch(function () {
            /* ignore */
          });
        });
      });
    }
    applyLanguage(initial);
  }

  window.MGRID_STRINGS = MGRID_STRINGS;
  window.applyLanguage = applyLanguage;
  window.mgridI18nApply = initLangToggle;
